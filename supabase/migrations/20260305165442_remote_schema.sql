CREATE POLICY "authenticated users can read songs from their collections" ON "public"."songs" FOR SELECT TO "authenticated" USING ((EXISTS ( SELECT 1
   FROM "public"."user_collections" "c"
  WHERE ((( SELECT "auth"."uid"() AS "uid") = "c"."user_id") AND ("songs"."collection_id" = "c"."collection_id")))));



