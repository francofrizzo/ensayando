CREATE POLICY "authenticated users can insert audio_tracks into their collecti" ON "public"."audio_tracks" FOR INSERT TO "authenticated" WITH CHECK ((EXISTS ( SELECT 1
   FROM ("public"."user_collections" "c"
     JOIN "public"."songs" "s" ON (("c"."collection_id" = "s"."collection_id")))
  WHERE ((( SELECT "auth"."uid"() AS "uid") = "c"."user_id") AND ("audio_tracks"."song_id" = "s"."id") AND ("c"."role" = ANY (ARRAY['admin'::"text", 'editor'::"text"]))))));



CREATE POLICY "authenticated users can insert songs into their collections" ON "public"."songs" FOR INSERT TO "authenticated" WITH CHECK ((EXISTS ( SELECT 1
   FROM "public"."user_collections" "c"
  WHERE ((( SELECT "auth"."uid"() AS "uid") = "c"."user_id") AND ("songs"."collection_id" = "c"."collection_id") AND ("c"."role" = ANY (ARRAY['admin'::"text", 'editor'::"text"]))))));



CREATE POLICY "authenticated users can read audio_tracks from their collection" ON "public"."audio_tracks" FOR SELECT USING ((EXISTS ( SELECT 1
   FROM ("public"."user_collections" "c"
     JOIN "public"."songs" "s" ON (("c"."collection_id" = "s"."collection_id")))
  WHERE ((( SELECT "auth"."uid"() AS "uid") = "c"."user_id") AND ("audio_tracks"."song_id" = "s"."id")))));



